package co_realtionalOperators;

public class OperatoryRelacyjne {
    public static void main(String[] args){
        boolean wyniczek;

        wyniczek = 3 == 3;
        System.out.println("Wynik 3==3: " + wyniczek);

        wyniczek = 2 == 3;
        System.out.println("Wynik 2==3: " + wyniczek);

        wyniczek = 2 != 3;
        System.out.println("Wynik 2!=3: " + wyniczek);

        wyniczek = 4 != 4;
        System.out.println("Wynik 4!=4: " + wyniczek);

        wyniczek = 3 > 4;
        System.out.println("Wynik 3>4: " + wyniczek);
    }
}
